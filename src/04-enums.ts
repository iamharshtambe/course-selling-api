// 📦 A box of status codes — giving names to meaningful numbers
enum StatusCodes {
   NotFound = 404, // Page vanished into the void 🚫
   Success = 200, // Everything's awesome ✅
   Accepted = 202, // Got it, working on it ⏳
   BadRequest = 400, // Something's fishy with the request 🐟
}

// Logging a sweet success 🍀
console.log(StatusCodes.Success);
